  function setupTopbarMotion() {
    var topbar = document.querySelector("[data-topbar]");
    if (!topbar) {
      return;
    }

    var stack = topbar.querySelector("[data-topbar-stack]");
    var caption = topbar.querySelector("[data-topbar-caption]");
    var links = Array.prototype.slice.call(topbar.querySelectorAll(".topbar-link"));
    if (!stack || !links.length) {
      return;
    }

    var activeLink = links.find(function (link) {
      return link.classList.contains("is-active");
    }) || links[0];

    function setCaption(link) {
      if (!caption || !link) {
        return;
      }
      caption.textContent = link.getAttribute("data-nav-note") || "";
    }

    setCaption(activeLink);

    links.forEach(function (link) {
      function activatePreview() {
        topbar.classList.add("is-hovering");
        link.classList.add("is-peeking");
        setCaption(link);
      }

      function clearPreview() {
        link.classList.remove("is-peeking");
        setCaption(activeLink);
        if (!links.some(function (item) { return item.matches(":hover, :focus-visible"); })) {
          topbar.classList.remove("is-hovering");
        }
      }

      link.addEventListener("mouseenter", activatePreview);
      link.addEventListener("focus", activatePreview);
      link.addEventListener("mouseleave", clearPreview);
      link.addEventListener("blur", clearPreview);
    });

    if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
      return;
    }

    var targetX = 0;
    var targetY = 0;
    var currentX = 0;
    var currentY = 0;

    topbar.addEventListener("pointermove", function (event) {
      var rect = topbar.getBoundingClientRect();
      var x = (event.clientX - rect.left) / rect.width - 0.5;
      var y = (event.clientY - rect.top) / rect.height - 0.5;
      targetX = x * 14;
      targetY = y * 8;
    });

    topbar.addEventListener("pointerleave", function () {
      targetX = 0;
      targetY = 0;
      topbar.classList.remove("is-hovering");
      setCaption(activeLink);
      links.forEach(function (link) {
        link.classList.remove("is-peeking");
      });
    });

    function animateTopbar() {
      currentX += (targetX - currentX) * 0.12;
      currentY += (targetY - currentY) * 0.12;
      stack.style.transform =
        "translate3d(" +
        currentX +
        "px," +
        currentY +
        "px,0) rotate(" +
        currentX * 0.12 +
        "deg)";
      window.requestAnimationFrame(animateTopbar);
    }

    animateTopbar();
  }

  function setupInteractiveTilt() {
    if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
      return;
    }

    document.querySelectorAll(".interactive-note").forEach(function (node) {
      node.addEventListener("pointermove", function (event) {
        var rect = node.getBoundingClientRect();
        var x = (event.clientX - rect.left) / rect.width - 0.5;
        var y = (event.clientY - rect.top) / rect.height - 0.5;
        node.style.transform =
          "rotateX(" +
          String(-y * 5) +
          "deg) rotateY(" +
          String(x * 6) +
          "deg)";
      });
      node.addEventListener("pointerleave", function () {
        node.style.transform = "";
      });
    });
  }

  function setupCursor() {
    if (
      window.matchMedia("(pointer: coarse)").matches ||
      window.matchMedia("(prefers-reduced-motion: reduce)").matches
    ) {
      return;
    }

    var dot = document.createElement("div");
    var ring = document.createElement("div");
    dot.className = "cursor-dot";
    ring.className = "cursor-ring";
    document.body.appendChild(dot);
    document.body.appendChild(ring);

    var cursor = { x: window.innerWidth / 2, y: window.innerHeight / 2, rx: window.innerWidth / 2, ry: window.innerHeight / 2 };

    document.addEventListener("pointermove", function (event) {
      cursor.x = event.clientX;
      cursor.y = event.clientY;
      dot.style.opacity = "1";
      ring.style.opacity = "1";
    });

    document.addEventListener("pointerleave", function () {
      dot.style.opacity = "0";
      ring.style.opacity = "0";
    });

    document.addEventListener("pointerdown", function () {
      ring.classList.add("is-active");
    });

    document.addEventListener("pointerup", function () {
      ring.classList.remove("is-active");
    });

    document.querySelectorAll("a, button").forEach(function (node) {
      node.addEventListener("mouseenter", function () {
        ring.classList.add("is-active");
      });
      node.addEventListener("mouseleave", function () {
        ring.classList.remove("is-active");
      });
    });

    function animate() {
      cursor.rx += (cursor.x - cursor.rx) * 0.18;
      cursor.ry += (cursor.y - cursor.ry) * 0.18;
      dot.style.transform = "translate(" + cursor.x + "px," + cursor.y + "px)";
      ring.style.transform = "translate(" + cursor.rx + "px," + cursor.ry + "px)";
      window.requestAnimationFrame(animate);
    }

    animate();
  }

  function setupClickBloom() {
    if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
      return;
    }

    document.addEventListener("pointerdown", function (event) {
      if (event.button && event.button !== 0) {
        return;
      }
      var bloom = document.createElement("span");
      bloom.className = "click-bloom";
      bloom.style.left = event.clientX + "px";
      bloom.style.top = event.clientY + "px";

      for (var i = 0; i < 8; i += 1) {
        var spark = document.createElement("span");
        spark.className = "spark";
        spark.style.setProperty("--angle", String(i * 45) + "deg");
        bloom.appendChild(spark);
      }

      document.body.appendChild(bloom);
      window.setTimeout(function () {
        bloom.remove();
      }, 760);
    });
  }

  function setupLightbox() {
    var modal = document.querySelector(".photo-lightbox");
    if (!modal) {
      modal = document.createElement("div");
      modal.className = "photo-lightbox";
      modal.innerHTML =
        '<button class="photo-lightbox-close" type="button" aria-label="Close photo">×</button><img alt="Expanded photo" />';
      document.body.appendChild(modal);
    }

    var image = modal.querySelector("img");
    var closeButton = modal.querySelector(".photo-lightbox-close");

    function close() {
      modal.classList.remove("is-open");
      image.removeAttribute("src");
    }

    document.addEventListener("click", function (event) {
      var trigger = event.target.closest("[data-lightbox-src]");
      if (trigger) {
        image.src = trigger.getAttribute("data-lightbox-src");
        modal.classList.add("is-open");
        return;
      }

      if (event.target === modal || event.target === closeButton) {
        close();
      }
    });

    document.addEventListener("keydown", function (event) {
      if (event.key === "Escape") {
        close();
      }
    });
  }

  function setupCollaborationChecklist() {
    var widget = document.querySelector("[data-collab-checklist]");
    if (!widget) {
      return;
    }

    var items = Array.prototype.slice.call(widget.querySelectorAll("[data-check-item]"));
    var stamps = Array.prototype.slice.call(widget.querySelectorAll("[data-check-stamp]"));
    var countNode = widget.querySelector("[data-check-count]");
    var noteNode = widget.querySelector("[data-check-note]");
    var sceneNode = widget.querySelector("[data-check-scene]");

    function update() {
      var checked = items.filter(function (item) {
        return item.classList.contains("is-checked");
      }).length;

      if (countNode) {
        countNode.textContent = String(checked) + "/" + String(items.length);
      }

      if (noteNode) {
        if (checked === 0) {
          noteNode.textContent = "Three signals. One road trip slowly coming into view.";
        } else if (checked === 1) {
          noteNode.textContent = "First layer: the dashboard, the driver, and the long road ahead.";
        } else if (checked === 2) {
          noteNode.textContent = "Second layer: the coast opens up with trees, sunset light, and birds.";
        } else if (checked === items.length) {
          noteNode.textContent = "Everyone is in the car now. The GIF drawer is open.";
        } else {
          noteNode.textContent = "Final layer: more people join the ride.";
        }
      }

      if (sceneNode) {
        sceneNode.setAttribute("data-check-state", String(checked));
      }

      stamps.forEach(function (stamp, index) {
        stamp.classList.toggle("is-active", index < checked);
      });

      widget.classList.toggle("is-unlocked", checked === items.length);
    }

    items.forEach(function (item) {
      item.addEventListener("click", function () {
        item.classList.toggle("is-checked");
        update();
      });
    });

    update();
  }

